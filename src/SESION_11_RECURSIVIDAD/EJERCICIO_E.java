    public static long SumaE(int x)
    {
        if(x==0){
            return 0;
        }else{
            if(x%2==0)
                return -fac(x+1)+SumaE(x-1);
            else
                return fac(x+1)+SumaE(x-1);
        }
    }
    
    public static int fac(int num)
    {
        if(num==0)
            return 1;
        else
            return num*fac(num-1);
    }
    
    
    
    

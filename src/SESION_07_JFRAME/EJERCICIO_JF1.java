    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String cod,apenom;
        int ep,ef,pp;
        double pf;
        DecimalFormat ob=new DecimalFormat("##.#");
        cod=txtCodigo.getText();
        apenom=txtNombre.getText();
        ep=Integer.parseInt(cbEp.getSelectedItem().toString());
        ef=Integer.parseInt(cbEf.getSelectedItem().toString());
        pp=Integer.parseInt(cbPp.getSelectedItem().toString());
        pf=(ep+ef+pp)/3.0;
        txtPf.setText(ob.format(pf)+"");
        atAlumno.append(cod+" "+apenom+" "+ep+" "+ef+" "+pp+" "+ob.format(pf)+"\n");
    }//GEN-LAST:event_btnCalcularActionPerformed

